function posToIndex( tileset, pos )
{
	var tilesetWidth = {
		"tileset": 16,
		"monsters": 6,
	}[tileset];
	return pos[0] + pos[1] * tilesetWidth;
}

Array.prototype.toIndex = function( tileset ) {
	var res = []
	for (var i = 0; i < this.length; i++) {
		res.push(posToIndex( tileset, this[i] ));
	}
	return res;
};
