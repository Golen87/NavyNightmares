function posToIndex( pos )
{
	const tilesetWidth = 16;
	return pos[0] + pos[1] * tilesetWidth;
}

Array.prototype.toIndex = function() {
	var res = []
	for (var i = 0; i < this.length; i++) {
		res.push(posToIndex( this[i] ));
	}
	return res;
};